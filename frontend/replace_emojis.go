func main() {
	files, err := filepath.Glob("templates/*.html")
	if err != nil {
		fmt.Println("Error finding files:", err)
		return
	}

	totalReplacements := 0
	for _, file := range files {
		content, err := os.ReadFile(file)
		if err != nil {
			fmt.Printf("Error reading %s: %v\n", file, err)
			continue
		}

		strContent := string(content)
		fileReplacements := 0

		for emoji, svg := range emojiToSVG {
			count := strings.Count(strContent, emoji)
			if count > 0 {
				strContent = strings.ReplaceAll(strContent, emoji, svg)
				fileReplacements += count
			}
		}

		if fileReplacements > 0 {
			err = os.WriteFile(file, []byte(strContent), 0644)
			if err != nil {
				fmt.Printf("Error writing %s: %v\n", file, err)
			} else {
				fmt.Printf("✓ %s — %d replacements\n", filepath.Base(file), fileReplacements)
				totalReplacements += fileReplacements
			}
		}
	}

	fmt.Printf("\nDone! %d emoji → SVG replacements across %d files.\n", totalReplacements, len(files))
	fmt.Println("\nAdd this CSS to style.css:")
	fmt.Println(`
.ax-icon {
    width: 1em;
    height: 1em;
    display: inline-block;
    vertical-align: -0.125em;
    stroke-linecap: round;
    stroke-linejoin: round;
}

.card-icon .ax-icon,
.feature-card-icon .ax-icon,
.category-icon .ax-icon {
    width: 1.5em;
    height: 1.5em;
}
`)
}
